//: ## 注释
//: 使用注释来将不需要执行的文本放入的代码当中，作为标记或者你自己的提醒。当 Swift 编译器在编译代码的时候会忽略掉你的注释。

//: Swift 中的注释和 C 的注释基本相同。单行注释用两个斜杠开头（ // ）：
//: - //                单行注释
//: -  /* ... */        多行注释
//: - 多行的注释以一个斜杠加一个星号开头（ /* ），以一个星号加斜杠结尾( */ )。多行注释。需要注意的是，多行注释可以嵌套，这点和其他语言不一样
/* 这是第一个多行注释的开头
 /* 这是第二个嵌套在内的注释块 */
 这是第一个注释块的结尾*/

//:内嵌多行注释，可以便捷地注释掉一大段代码块，即使这段代码块中已经有了多行注释。

