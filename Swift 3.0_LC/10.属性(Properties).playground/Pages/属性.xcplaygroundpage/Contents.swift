//: ## 属性 (Properties)
//: 本页包含内容：
//: - 存储属性（Stored Properties）
//: - 计算属性（Computed Properties）
//: - 属性观察器（Property Observers）
//: - 全局变量和局部变量（Global and Local Variables）
//: - 类型属性（Type Properties）
//: ---
//: - 属性: 将 值 跟 特定的 类、结构或枚举关联。
//: - 存储属性: 存储常量或变量作为实例的一部分，而计算属性计算（不是存储）一个值, 存储属性只能用于类和结构体.
//: - 计算属性: 计算一个值，计算属性可以用于`类`、`结构体`和`枚举`
//: - `存储属性`和`计算属性`通常与特定类型的实例关联，但属性也可以直接作用于类型本身，称为`类型属性`
//: - 还可以定义属性观察者来监控属性值的变化，以触发一个自定义的操作

