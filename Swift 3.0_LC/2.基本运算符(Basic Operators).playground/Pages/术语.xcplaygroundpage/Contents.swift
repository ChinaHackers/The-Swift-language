//: ### 术语
/*:
 操作符都是一元、二元或三元:
 
 · 一元操作符操作单个对象 (如 -a)。一元前缀操作符出现在对象前(如 !b)，一元后缀操作符在对象后出现 (如 i++)。
 
 · 二元操作符操作两个对象(如 2 + 3)，并且操作符位于两个元素中间。
 
 · 三元操作符对两个对象进行操作。与C一样，Swift仅支持一个三元操作符：三元条件操作符 (a ? b : c)。(?前面一定需要至少一个空格)
 
 操作符所影响的值被称为操作数。表达式1 + 2中，符号 + 是一个二元运算符并且两个操作数分别为 1 和 2。
 */

