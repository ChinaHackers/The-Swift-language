//: ### 比较运算符. 通常用在条件语句
/*:
 Swift支持所有标准c的比较运算符
 
 等于 (a == b)
 
 不等于(a != b)
 
 大于 (a > b)
 
 小于 (a < b)
 
 大于等于 (a >= b)
 
 小于等于(a <= b)
 
 `Swift` 提供两个恒等运算符`(=== and !==)`，用它来测试两个对象引用是否来自于同一个对象实例
 
 */
1 == 1 // true, 因为1等于1
2 != 1 // true, 因为2不等于1
2 > 1 // true, 因为 2 > 1成立
1 < 2 // true, 因为1 < 2 成立
1 >= 1 // true, 因为1是大于或等于1
2 <= 1 // false, 因为2是小于或等于1

